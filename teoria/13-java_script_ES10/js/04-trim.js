const correo = '    correo@correo.com    ';

// trimpermite eliminar el espacio en blanco de una cadena de texto

console.log(correo);
console.log(correo.trimStart()); // Elimina el espacio en blanco del principio
console.log(correo.trimEnd()); // Elimina el espacio en blanco del final de la cadena
console.log(correo.trim()); // Elimina el espacio en blanco del principio y el final